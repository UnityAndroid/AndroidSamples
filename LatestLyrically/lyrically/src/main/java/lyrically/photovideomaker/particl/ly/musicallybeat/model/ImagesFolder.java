package lyrically.photovideomaker.particl.ly.musicallybeat.model;

import java.util.ArrayList;

public class ImagesFolder {

    public String folder_name;
    public ArrayList<String> folder_images;

    public ImagesFolder() {
    }

    public String getFolder_name() {
        return folder_name;
    }

    public void setFolder_name(String folder_name) {
        this.folder_name = folder_name;
    }

    public ArrayList<String> getFolder_images() {
        return folder_images;
    }

    public void setFolder_images(ArrayList<String> folder_images) {
        this.folder_images = folder_images;
    }
}
